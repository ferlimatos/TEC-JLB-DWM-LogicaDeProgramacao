# Exercício 2
# Acesso e Modificação de Elementos
# Crie uma lista de números inteiros: [10, 20, 30, 40, 50]. 
# a. Imprima o elemento na posição 3. 
# b. Modifique o elemento na posição 1 para o valor 25. 
# c. Imprima a lista modificada.

numeros_inteiros = [10, 20, 30, 40, 50]

# a. Imprima o elemento na posição 3. 
elemento_3 = numeros_inteiros[3]
print(f"Acessando o elemento na posição 3: {elemento_3}")

# b. Modifique o elemento na posição 1 para o valor 25. 
numeros_inteiros[1] = 25

# c. Imprima a lista modificada.
print(f"Lista após modificação: {numeros_inteiros}")
